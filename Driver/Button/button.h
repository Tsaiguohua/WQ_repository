#ifndef BUTTON_H
#define BUTTON_H

#include "stm32f4xx.h"
#include <stdint.h>

//////////////////////////////////////////////////////////////////////////////////
// 按键驱动 - FreeRTOS版本
//
// 功能说明：
//   - 按键输入检测（PC0）
//   - 控制OLED显示开关
//
// 硬件配置：
//   - 按键: PC0（上拉输入）
//   - 注意: 已从PC5更换,避免与Channel2控制引脚(PC5)冲突
//
// 创建日期: 2026/01/03
// 作者: 蔡国华
//////////////////////////////////////////////////////////////////////////////////

/*============================================================================
 *                          硬件配置宏定义
 *===========================================================================*/

/* 按键引脚配置（PC0）- 已从PC5更换,避免与Channel2控制引脚冲突 */
#define BUTTON_PORT GPIOC
#define BUTTON_PIN GPIO_Pin_0
#define BUTTON_GPIO_CLK RCC_AHB1Periph_GPIOC

/*============================================================================
 *                          函数声明
 *===========================================================================*/

/**
 * @brief  初始化按键GPIO
 */
void Button_Init(void);

/**
 * @brief  读取按键状态
 * @retval 0=按下，1=释放
 */
uint8_t Button_GetState(void);

/**
 * @brief  扫描按键（带消抖）
 * @retval 1=按键按下事件，0=无事件
 */
uint8_t Button_Scan(void);

#endif

#ifndef SENSOR_COMMON_H
#define SENSOR_COMMON_H

#include <stdbool.h>
#include <stdint.h>

//////////////////////////////////////////////////////////////////////////////////
// 水质传感器公共定义 - 面向对象风格
//
// 功能说明：
//   1. 定义所有水质传感器的Modbus从机地址
//   2. 定义传感器状态枚举
//   3. 定义通用数据结构（如Y4000多参数数据、COD数据等）
//
// 传感器地址分配（出厂默认）：
//   - 0x01: COD传感器（温度/COD/TOC/浊度）
//   - 0x02: CDOM传感器（有色溶解有机物）
//   - 0x03: CHL传感器（叶绿素）
//   - 0x04: PH传感器
//   - 0x05: DO传感器（溶解氧）
//   - 0x06: SAL传感器（盐度）
//   - 0x07: Y4000多参数母体（可同时读取PH/DO/SAL）
//
// 创建日期: 2025/12/31
// 作者: 蔡国华
//////////////////////////////////////////////////////////////////////////////////

/*============================================================================
 *                          传感器Modbus地址定义
 * 注意：这些是传感器的出厂默认地址，如果修改过需要相应更新
 *===========================================================================*/

#define SENSOR_ADDR_COD 0x01   // COD/浊度传感器地址
#define SENSOR_ADDR_CDOM 0x02  // CDOM传感器地址
#define SENSOR_ADDR_CHL 0x03   // 叶绿素传感器地址
#define SENSOR_ADDR_PH 0x04    // PH传感器地址（单独使用时）
#define SENSOR_ADDR_DO 0x05    // DO传感器地址（单独使用时）
#define SENSOR_ADDR_SAL 0x06   // SAL传感器地址（单独使用时）
#define SENSOR_ADDR_Y4000 0x07 // Y4000多参数母体地址

/*============================================================================
 *                          传感器状态枚举
 * 用于跟踪传感器的连接状态
 *===========================================================================*/

typedef enum {
  SENSOR_STATUS_UNKNOWN = 0,  // 未知状态（初始化前）
  SENSOR_STATUS_CONNECTED,    // 已连接（通信正常）
  SENSOR_STATUS_DISCONNECTED, // 未连接（通信失败）
  SENSOR_STATUS_ERROR         // 错误状态（数据异常）
} sensor_status_t;

/*============================================================================
 *                          数据有效性标识
 * 使用999作为无效值标记，因为正常传感器不会返回这个值
 *===========================================================================*/

#define SENSOR_VALUE_INVALID 999.0f // 无效数据标识

/*============================================================================
 *                          Y4000多参数数据结构
 * Y4000母体可以一次性读取PH/DO/SAL三个参数，提高效率
 *===========================================================================*/

typedef struct {
  float do_value;  // 溶解氧值（mg/L）
  float ph_value;  // PH值（0-14）
  float sal_value; // 盐度值（ppt）
  float atm_value; // 大气压（kPa）
  bool valid;      // 数据有效标志（true=有效）
} y4000_data_t;

/*============================================================================
 *                          COD传感器数据结构
 * COD传感器可同时读取温度、COD、TOC、浊度四个参数
 *===========================================================================*/

typedef struct {
  float temp; // 水温（℃）
  float cod;  // COD值（mg/L）
  float toc;  // TOC值（mg/L）
  float tur;  // 浊度（NTU）
  bool valid; // 数据有效标志
} cod_data_t;

#endif

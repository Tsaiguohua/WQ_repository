#ifndef UART4_MQTT_H
#define UART4_MQTT_H

#include "app.h"
#include <stdbool.h>
#include <stdint.h>

//////////////////////////////////////////////////////////////////////////////////
// UART4 MQTT指令接收驱动 - FreeRTOS版本
//
// 功能说明:
//   使用UART4接收MQTT模块下发的JSON格式指令
//   中断接收完整JSON数据包后释放信号量通知Command任务处理
//
// 硬件配置:
//   - UART: UART4
//   - TX引脚: PC10
//   - RX引脚: PC11
//   - 波特率: 115200
//   - 数据位: 8位
//   - 停止位: 1位
//   - 校验位: 无
//
// 工作原理:
//   1. 中断检测JSON数据包边界 ('{' 到 '}')
//   2. 接收完整数据包到缓冲区
//   3. 释放信号量通知Command_Task处理
//
// 创建日期: 2026/01/06
// 作者: 蔡国华
//////////////////////////////////////////////////////////////////////////////////

/*============================================================================
 *                          配置宏定义
 *===========================================================================*/

/* 接收缓冲区大小 (需要容纳UPFILE数据包: 头部~20字节 + 数据256字节) */
// #define MQTT_RX_BUF_SIZE 512
#define MQTT_RX_BUF_SIZE 2048

/*============================================================================
 *                          类型定义
 *===========================================================================*/

/**
 * @brief UART接收消息结构体（用于队列传递）
 */
typedef struct {
  uint8_t *pBuffer; /**< 指向接收缓冲区的指针 */
  uint16_t length;  /**< 接收到的数据长度 */
} UartRxMsg_t;

/*============================================================================
 *                          API函数声明
 *===========================================================================*/

/**
 * @brief  初始化UART4 MQTT接收
 * @note   配置GPIO、UART4、中断,在main.c中调用
 */
void UART4_MQTT_Init(void);

/**
 * @brief  打印UART4 DMA状态（调试用）
 */
void UART4_PrintDMAStatus(void);

#endif
